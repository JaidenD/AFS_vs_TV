    ["Fixed bicycle-model yaw-moment authority; replace with plant-specific " ...
     "feasible limits. Zero-crossing detection is disabled only on this " ...
     "hard limiter to prevent event chattering; the top-level harness " ...
     "therefore uses tv.maxSolverStep to resolve the fast control loop."]);

add_block("simulink/Math Operations/Sum", modelName + "/SaturationError", ...
    "Inputs", "+-", "IconShape", "round", ...
    "Position", [1040 415 1070 455]);
