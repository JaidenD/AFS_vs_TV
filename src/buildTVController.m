    ["Project-specific back-calculation gain. The paper confirms " ...
     "anti-windup but does not publish its topology or gain."]);

connect(modelName, "FeedbackMoment/1", "TotalMoment/1", "Mz_fb", true);
connect(modelName, "FeedforwardMoment/1", "TotalMoment/2", "Mz_ff", true);
connect(modelName, "TotalMoment/1", "MomentLimit/1", "Mz_unsat", true);
