end

function createDocumentation(modelName)
paperText = strjoin({ ...
    'PAPER BASIS: De Novellis et al., IEEE TVT 63(8), 2014, Sec. II-D, II-E, III-A.', ...
    'Published elements: tau=0.3 s reference filter; feedforward + PID; saturation/anti-windup.', ...
    'Active published 90-km/h gains: Kp=80 kNms/rad, Ki=0.004 Nm/rad, Kd=0.8 Nms^2/rad.', ...
    'Paper requires speed scheduling outside its narrow speed range but does not publish the schedule.', ...
    'Project adaptations: path-curvature reference; analytical E-Class feedforward; fixed Mz limit; back-calculation anti-windup; no wheel allocator.'}, newline);
ann = Simulink.Annotation(modelName, paperText);
ann.Position = [180 15 980 65];
ann.FontSize = 10;

ann = Simulink.Annotation(modelName, ...
    "Reference yaw rate (paper Eq. 7 + tau = 0.3 s filter)");
ann.Position = [180 90 480 110];
ann.FontWeight = "bold";

ann = Simulink.Annotation(modelName, ...
    "Parallel PID feedback with explicit back-calculation anti-windup");
ann.Position = [600 75 960 95];
ann.FontWeight = "bold";

ann = Simulink.Annotation(modelName, ...
    "Quasi-static E-Class feedforward adaptation");
ann.Position = [350 330 650 350];
ann.FontWeight = "bold";
end

function chart = findEMChart(blockPath)
root = sfroot;
chart = root.find("-isa", "Stateflow.EMChart", "Path", blockPath);
if isempty(chart)
    error("buildTVController:ChartNotFound", ...
        "Could not locate MATLAB Function chart at %s.", blockPath);
end
chart = chart(1);
end

function lineHandle = connect(modelName, source, destination, signalName, logSignal)
lineHandle = add_line(modelName, source, destination, "autorouting", "on");
if strlength(signalName) > 0
    try
        set_param(lineHandle, "Name", signalName);
    catch exception
        % Bus Selector outputs already carry propagated element labels
        % (for example, <beta>) and Simulink prohibits overriding them.
        % Preserve that informative propagated name, but rethrow unrelated
        % signal-naming failures so the builder cannot hide real defects.
        if ~contains(exception.message, "Bus Selector")
            rethrow(exception);
        end
    end
end
if logSignal
    try
        set_param(lineHandle, "DataLogging", "on");
    catch exception
        % Some propagated bus-element lines do not expose DataLogging as a
        % line parameter. Those elements remain available through the
        % logged VehicleState/Reference buses in the experiment harness.
        if ~contains(exception.message, "DataLogging")
            rethrow(exception);
        end
    end
end
end
